#include<iostream>

int main(){

    int a;
    std::cout<<"The Size of Int: "<<sizeof(a)<<std::endl;

    float b;
    std::cout<<"The Size of Float: "<<sizeof(b)<<std::endl;

    char c;
    std::cout<<"The Size of Char: "<<sizeof(c)<<std::endl;

    bool d;
    std::cout<<"The Size of Boolean: "<<sizeof(d)<<std::endl;

    long int li;
    std::cout<<"The Size of Long Int: "<<sizeof(li)<<std::endl;

    short int si;
    std::cout<<"The Size of Short Int: "<<sizeof(si)<<std::endl;
}